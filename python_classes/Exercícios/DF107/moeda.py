def aumentar(v, p):
    """
    FUNÇÃO AUMENTAR
    Parâmetro p: número da qual se deseja cálcular o aumento
    Parâmetro aum: cálculo de aumento de 30% sobre p.
    """
    return v + v * (p / 100)


def metade(p):
    return p / 2


def dobro(p):
    return p * 2


def reduzir(v, p):
    return v - v * (p / 100)